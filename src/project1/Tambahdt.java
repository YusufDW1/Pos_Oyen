    private void bt_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_simpanActionPerformed
        String name = txt_nama.getText();
        String user = txt_username.getText();
        String pass = new String(txt_password.getPassword());
        String level = cmb_level.getSelectedItem().toString();

        //pengecekan

        try {
            Connection K = DatabaseConnection.Go();
            String Q = "INSERT INTO user"
            + "(fullname,username,password,id_level) "
            + "VALUES (?,?,?,?)";
            PreparedStatement PS = K.prepareStatement(Q);
            
            PS.setString(1, name);
            PS.setString(2, user);
            PS.setString(3, pass);
            PS.setString(4, level);
            PS.executeUpdate();
            
             // Menambahkan log aktivitas untuk mencatat data yang berhasil disimpan
            Logging.logActivity("Data berhasil ditambahkan: Nama = " + name + ", Username = " + user + ", Level = " + level);

            HalamanAdmin.viewData("");
            JOptionPane.showMessageDialog(this, "Data berhasil disimpan");
            txt_nama.requestFocus();
            
        } catch (Exception e) {
             JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
        }
    }//GEN-LAST:event_bt_simpanActionPerformed

