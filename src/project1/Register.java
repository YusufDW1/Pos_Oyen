    private void btn_registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_registerActionPerformed
           String username = txt_username.getText();
    String password = new String(txt_password.getPassword());
    String namaUser = txt_nama.getText();  // Asumsi Anda memiliki text field untuk nama
    int idLevel = 4;  // Mengatur level user ke 4 untuk pelanggan

    // Validasi input
    if (namaUser.isEmpty() || username.isEmpty() || password.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Semua field harus diisi!");
    } else {
        // Panggil method untuk menyimpan user ke database
        UserData.saveUser(username, password, namaUser, idLevel);
        JOptionPane.showMessageDialog(null, "Pendaftaran berhasil!");
        new Login().setVisible(true);
        dispose();  // Menutup jendela Register
    }

    }//GEN-LAST:event_btn_registerActionPerformed

