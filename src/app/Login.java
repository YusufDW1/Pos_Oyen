    private void LoginNow() {                                          
    String user = txt_username.getText();
    String pass = new String(txt_password.getPassword());

    try {
        Connection c = Koneksi.Go();
        if (c == null) {
            JOptionPane.showMessageDialog(this, "Gagal menghubungkan ke database.");
            return;
        }
        
        // Menggunakan PreparedStatement untuk mencegah SQL injection
        String sql = "SELECT * FROM `user` WHERE username = ? AND password = ?;";
        PreparedStatement stmt = c.prepareStatement(sql); 
        stmt.setString(1, user);
        stmt.setString(2, pass);
        
        ResultSet r = stmt.executeQuery();
        int status = 0;
        int id_user;
        String fullname, us, ps, lv = null;
        UserProfile up = new UserProfile();

        while (r.next()) {                 
            id_user = r.getInt("id_user");
            fullname = r.getString("fullname");
            us = r.getString("username");
            ps = r.getString("password");
            lv = r.getString("id_level");
            up.setId(id_user);
            up.setFullname(fullname);
            up.setUsername(us);
            up.setPassword(ps);
            up.setLevel(lv); 
            
            status++;
        }

        if (status > 0) {
            // Login berhasil
            JOptionPane.showMessageDialog(this, "Sukses Login");
            Logging.logActivity("Login sukses: User " + user + " dengan level " + lv);

            switch (lv) {
                case "kasir" -> {
                    this.setVisible(false);
                    HalamanKasir HK = new HalamanKasir(up);
                    HK.setVisible(true);
                }
                case "admin" -> {
                    this.setVisible(false);
                    HalamanAdmin HA;
                    HA = new HalamanAdmin(up);
                    HA.setVisible(true);
                }

                case "owner" -> {
                    this.setVisible(false);
                    HalamanOwner HO = new HalamanOwner(up);
                    HO.setVisible(true);
                }
                default -> {
                    JOptionPane.showMessageDialog(this, "Level pengguna tidak dikenal.");
                }
            }
        } else {
            // Login gagal
            JOptionPane.showMessageDialog(this, """
                                                GAGAL Login
                                                Username/Password tidak valid""");
            txt_password.requestFocus();
            Logging.logActivity("Login gagal: Username/Password tidak valid untuk user " + user);
        }

    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        Logging.logActivity("Error " + e.getMessage());
    }
    }
